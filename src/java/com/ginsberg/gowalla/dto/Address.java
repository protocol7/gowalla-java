package com.ginsberg.gowalla.dto;

import java.io.Serializable;

/**
 * Implementation of an address, as returned on some Gowalla spots.  
 * 
 * Note: street address is not on here because the API doesn't return
 * valid data.  I don't want to mislead or knowingly provide bad data,
 * so I'll add it when/if the server returns something that isn't clearly
 * bogus.
 * 
 * @author Todd Ginsberg
 *
 */
public class Address implements Serializable {

	private static final long serialVersionUID = -351522070008113333L;
	protected String locality;
	protected String region;
	
	public Address() {
		super();
	}

	public String getLocality() {
		return locality;
	}

	public void setLocality(String locality) {
		this.locality = locality;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((locality == null) ? 0 : locality.hashCode());
		result = prime * result + ((region == null) ? 0 : region.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Address other = (Address) obj;
		if (locality == null) {
			if (other.locality != null)
				return false;
		} else if (!locality.equals(other.locality))
			return false;
		if (region == null) {
			if (other.region != null)
				return false;
		} else if (!region.equals(other.region))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return String.format("Address[locality=%s, region=%s]", locality, region);
	}

}
