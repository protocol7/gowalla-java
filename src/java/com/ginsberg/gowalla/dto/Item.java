package com.ginsberg.gowalla.dto;

import java.io.Serializable;

/**
 * Representation of an Item returned by the Gowalla system. 
 * 
 * @author Todd Ginsberg
 */
public class Item implements Serializable, Id<Item> {

	private static final long serialVersionUID = 268745508655063846L;
	private int issue_number;
	private String name;
	private String determiner;
	private String url;
	private String image_url;
	private int id;

	public Item() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getIssueNumber() {
		return issue_number;
	}

	public void setIssueNumber(int issueNumber) {
		this.issue_number = issueNumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDeterminer() {
		return determiner;
	}

	public void setDeterminer(String determiner) {
		this.determiner = determiner;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getImageUrl() {
		return image_url;
	}

	public void setImageUrl(String imageUrl) {
		this.image_url = imageUrl;
	}


	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Item other = (Item) obj;
		if (id != other.id)
			return false;
		return true;
	}

	@Override
	public String toString() {
		return String.format("Item[%s %s, issue=%d, id=%d]", determiner, name, issue_number, id);
	}

}
