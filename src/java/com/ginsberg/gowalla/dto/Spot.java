package com.ginsberg.gowalla.dto;

import java.io.Serializable;

/**
 * Bare minimum data returned in all spot-related requests.
 * 
 * @author Todd Ginsberg
 */
public class Spot implements Serializable, Id<Spot> {

	protected static final long serialVersionUID = -2945532609285534099L;
	protected String name;
	protected String url;
	protected int id;
	protected String image_url;

	public Spot() {
		super();
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getImageUrl() {
		return image_url;
	}

	public void setImageUrl(String imageUrl) {
		this.image_url = imageUrl;
	}

	@Override
	public String toString() {
		return String.format("Spot[id=%d, name=%s]", id, name);
	}

}
